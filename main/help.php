	<div class="faq-box" style="left:260px; width:955px;">
		<h4><b>How do I create an event?</b></h4>
		<p id="how_to_create_event">To create an event, start by going <a href="?act=day">here</a> to the day view. At the top of the screen, you should
		see a "Create Event" button which, when clicked, should bring up a form where you can enter the exact details of your event!</p>
		
		<h4><b>Where will my event show up?</b></h4>
		<p id="what_do_i_do_after">After successfully creating an event, it should show not only <a href="?act=day">here</a> on your day calendar, 
		but <a href="?act=month">here</a> on your monthly calendar as well!</p>
		
		<h4><b>Will I be able to change event settings after I create an event?</b></h4>
		<p id="can_i_change_details">After successfully creating an event, one can change some selected event settings by clicking on the event itself on 
		either the <a href="?act=day">day</a> or <a href="?act=month">month</a> calendar.</p>
		
		<h4><b>How does the event priority system work?</b></h4>
		<p id="event_priority_system">The priority system works by coloring individual events based on their priority. A red event will be an event with 
		a high priority, while a yellow event will have a medium priority, and a green event will have a low priority. You may elect to give an event no 
		priority, and therefore it will not be highlighted. Also, holidays shown on the calendar have no priority, so they are not highlighted as well.</p>
